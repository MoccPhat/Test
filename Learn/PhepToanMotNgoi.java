package Learn;
public class PhepToanMotNgoi {
    public static void main(String[] agrs){
        // Khai báo biến
        int a = 5;
        boolean b = false;
        System.out.println(" -a => gia tri am: " +(-a));
        System.out.println(" +a => gia tri duong: " +(+a));
        System.out.println("!b => gia tri phu dinh: " +(!b));
        System.out.println("----------------");
        System.out.println("Gia tri: " +a);
        System.out.println("Gia tri ++a: "+ (++a));
        System.out.println("Gia tri a++: "+ (a++));
        System.out.println("Tang gia tri: "+a);
        System.out.println("Gia tri --a: "+ (--a));
        System.out.println("Gia tri a++: "+ (a--));
        System.out.println("Dao bit: "+(~a));
    }
}

